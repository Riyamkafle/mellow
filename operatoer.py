# Import the math module for advanced arithmetic operations
import math

# Basic arithmetic operations
a = 10
b = 3

# Addition
addition = a + b
print("Addition:", addition)

# Subtraction
subtraction = a - b
print("Subtraction:", subtraction)

# Multiplication
multiplication = a * b
print("Multiplication:", multiplication)

# Division
division = a / b
print("Division:", division)

# Modulus (remainder)
modulus = a % b
print("Modulus:", modulus)

# Exponentiation
exponentiation = a ** b
print("Exponentiation:", exponentiation)

# Advanced arithmetic operations using the math module
print("Square root of a:", math.sqrt(a))
print("Absolute value of b:", abs(b))
print("Power of a and b:", math.pow(a, b))